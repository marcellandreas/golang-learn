package main

import "fmt"

func main() {
	// boolean
	var isRaining bool = true
	var isSunny bool = false

	isLoggedIn := true
	hasPermission := false

	// menampilkan nilai variabel boolean
	fmt.Println("Boolean Values:")
	fmt.Printf("isRaining: %v\n", isRaining)
	fmt.Printf("isSunny: %v\n", isSunny)
	fmt.Printf("isLoggedIn: %v\n", isLoggedIn)
	fmt.Printf("hasPermission: %v\n", hasPermission)
}
